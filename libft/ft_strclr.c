#include "libft.h"

void ft_strclr(char *s)
{
	int i;
	int j;

	i = 0;
	j = ft_strlen (s);
	if (s != NULL)
	{	
		while (i < j)
		{
			if (s[i] != '\0')
				s[i] = '\0';
			i++;
		}
	}
}
