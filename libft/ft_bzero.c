#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*str;
	size_t	i;

	i = 0;
	str = (char *)s;
	while ( n > 0)
	{
		str[i] = '\0';
		i++;
		n--;
	}
}
