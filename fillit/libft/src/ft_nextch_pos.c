#include "libft.h"

int		ft_nextch_pos(const char *str)
{
	int i;

	i = 0;
	if (str)
	{
		while (ft_check_charset(str[i], " \t\n"))
		{
			i++;
		}
		return (i);
	}
	return (0);
}
